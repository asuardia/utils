{-# LANGUAGE DeriveDataTypeable #-}
{-# OPTIONS_GHC -XFlexibleInstances #-}


--------------------------------------------------------------------------
--------------------------------- Module ---------------------------------
--------------------------------------------------------------------------
module Utils.Containers   
    ( 
     Container1D (..), Container2D (..), Container3D (..),
     Container1D' (..), Container2D' (..), Container3D' (..)
    ) where

--------------------------------------------------------------------------
------------------------------- Imports ----------------------------------
--------------------------------------------------------------------------

--------------------------------------------------------------------------
------------------------------- Data -------------------------------------
--------------------------------------------------------------------------

data Container1D a = Container1D {
                                     c1Xs    :: [a],
                                     c1Ys    :: [Double],
                                     c1Inerp :: a -> Double
                                 }  
--------------------------------------------------------------------------

data Container1D' a = Container1D' {
                                       c1Xs'    :: [a],
                                       c1Ys'    :: [Double],
                                       c1Inerp' :: a -> Double,
                                       c1Sens'  :: a -> [Double]
                                   }  
--------------------------------------------------------------------------

data Container2D a b = Container2D {
                                       c2Xs    :: [a],
                                       c2Ys    :: [b],
                                       c2Zs    :: [[Double]],
                                       c2Inerp :: a -> b -> Double
                                   }  
--------------------------------------------------------------------------

data Container2D' a b = Container2D' {
                                         c2Xs'    :: [a],
                                         c2Ys'    :: [b],
                                         c2Zs'    :: [[Double]],
                                         c2Inerp' :: a -> b -> Double,
                                         c2Sens'  :: a -> b -> [[Double]]
                                     }  
--------------------------------------------------------------------------

data Container3D a b c = Container3D {
                                         c3Xs    :: [a],
                                         c3Ys    :: [b],
                                         c3Zs    :: [c],
                                         c3Vs    :: [[[Double]]],
                                         c3Inerp :: a -> b -> c -> Double
                                     }  
--------------------------------------------------------------------------

data Container3D' a b c = Container3D' {
                                           c3Xs'    :: [a],
                                           c3Ys'    :: [b],
                                           c3Zs'    :: [c],
                                           c3Vs'    :: [[Double]],
                                           c3Inerp' :: a -> b -> c -> Double,
                                           c3Sens'  :: a -> b -> c -> [[[Double]]]
                                       }  
